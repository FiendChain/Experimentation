# Importing the math library
import math

# Basic operators
x = 5
y = 6
var = x + y     # 13
var = x - y     # -1
var = x * y     # 30
var = x / y     # 0.833...
var = x % y     # 5/6 = 0 remainder 5 -> 5
var = x ** y    # 5^6 = 15625

# Using math constants from the module
print(math.pi)  # Prints pi
print(math.e)   # Prints euler's number